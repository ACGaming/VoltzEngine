/**
 * The Interface IRotatable.
 */
public interface IRotatable
{
    
    /**
     * Gets the direction.
     *
     * @return the direction
     */
    public abstract byte getDirection();

    /**
     * Sets the direction.
     *
     * @param byte0 the new direction
     */
    public abstract void setDirection(byte byte0);
}
